            # reject Volatility 2 (/usr/local/bin/vol.py on stock SIFT)
            if "$c" --help 2>&1 | grep -qiE 'volatility 3|framework 2\.'; then
                VOL3_PATH="$(command -v "$c")"
                break
            fi
        fi
    done
fi
