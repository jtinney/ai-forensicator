MISSING_COUNT=0

# ---------- helpers ----------
bin_check() {
    # bin_check <command> — tries --version, -V, -v in that order; suppresses error banners
    local cmd="$1"
    if ! command -v "$cmd" >/dev/null 2>&1; then
        printf "| \`%s\` | MISSING | — | — |\n" "$cmd"
        (( MISSING_COUNT++ )) || true
        return 1
    fi
    local path; path="$(command -v "$cmd")"
    local ver=""
    for varg in --version -V -v; do
        local out; out="$("$cmd" "$varg" 2>&1 | head -n1 | tr -d '\r')"
        # reject usage/error lines
        if [[ -n "$out" ]] && [[ "$out" != *invalid* ]] && [[ "$out" != *Unknown* ]] \
           && [[ "$out" != *usage:* ]] && [[ "$out" != *Usage:* ]]; then
            ver="$out"
            break
        fi
    done
    printf "| \`%s\` | OK | \`%s\` | %s |\n" "$cmd" "$path" "${ver:-present}"
    return 0
}

pkg_check() {
    # pkg_check <dpkg-pkg-name>
    local pkg="$1"
    if dpkg -s "$pkg" >/dev/null 2>&1; then
        local ver; ver="$(dpkg -s "$pkg" 2>/dev/null | awk -F': ' '/^Version:/ {print $2; exit}')"
        printf "| \`%s\` | INSTALLED | %s |\n" "$pkg" "${ver:-n/a}"
        return 0
    else
        printf "| \`%s\` | MISSING | — |\n" "$pkg"
        (( MISSING_COUNT++ )) || true
        return 1
    fi
}

py_check() {
    # py_check <module>
    local mod="$1"
    if python3 -c "import $mod" >/dev/null 2>&1; then
        local ver; ver="$(python3 -c "import $mod; print(getattr($mod,'__version__','n/a'))" 2>/dev/null)"
        printf "| \`%s\` | OK | %s |\n" "$mod" "${ver:-n/a}"
        return 0
    else
        printf "| \`%s\` | MISSING | — |\n" "$mod"
        (( MISSING_COUNT++ )) || true
        return 1
    fi
}

path_check() {
    # path_check <path> <label>
    local p="$1"; local label="$2"
    if [[ -e "$p" ]]; then
        printf "| %s | PRESENT | \`%s\` |\n" "$label" "$p"
        return 0
    else
        printf "| %s | MISSING | \`%s\` |\n" "$label" "$p"
        (( MISSING_COUNT++ )) || true
        return 1
    fi
}

